/* This module demonstrates the use of various console methods in JavaScript.
 * The console object provides access to the browser's debugging console and is commonly used for logging information, errors, warnings, and other messages during development.
 * 
 * 1. console.log(): Used to log general information to the console.
 * 2. console.table(): Used to display data in a tabular format, making it easier to read and analyze. 
 * 3. console.error(): Used to log error messages to the console, typically displayed in red.
 * 4. console.warn(): Used to log warning messages to the console, typically displayed in yellow.
 * 5. console.info(): Used to log informational messages to the console, typically displayed in blue.
 */

console.log("Hello, World!");
console.table({ name: "Alice", age: 30, city: "New York" });
console.error("This is an error message.");
console.warn("This is a warning message.");
console.info("This is an informational message.");
